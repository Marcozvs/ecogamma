//Fazendo com que ao usuário clicar em um campo ele fique verde

//Interação no campo nome
function inputIconesNome()
{
    //Selecionando elementos do nome no formulário do login
    const labelIconesNome = document.getElementById("label-icones-nome");
    const inputIconesNome = document.getElementById("input-icones-nome");
    const inputIconesNomeSpan = document.getElementById("input-icones-nome-span");
    const inputIconesNomeLetra = document.getElementById("nome");

    //Deixando os elementos do input nome verdes

    labelIconesNome.style.color = "#34B132";
    inputIconesNome.style.border = "2px solid #34B132";
    inputIconesNomeSpan.style.color = "#34B132";
    inputIconesNomeLetra.style.color = "#34B132";
}

